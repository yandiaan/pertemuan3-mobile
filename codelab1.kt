// Fungsi main di bawah ini adalah titik masuk (entry point) program Kotlin.
// Ini berarti bahwa fungsi main adalah fungsi yang pertama kali dijalankan.
fun main() {
    // Perintah println digunakan untuk mencetak nilai dan menambahkan baris baru sesuai dengan parameter fungsi `println`.
    println("I'm")
    println("Dian ")
    
    // Perintah print memiliki fungsi yang sama seperti println, tetapi tidak mencetak baris baru.
    print("Setiawan!")
}
